// ================= Scoring Logic =================

export function calculateCareerScores(answers) {
    // answers = { questionId: selectedScore (1-5) }

    const totalScores = {};

    questions.forEach(q => {
        const answerScore = answers[q.id] || 0;

        Object.entries(q.scores).forEach(([career, weight]) => {
            if (!totalScores[career]) totalScores[career] = 0;
            totalScores[career] += answerScore * weight;
        });
    });

    return totalScores;
}

export function getTopCareerRecommendation(scoreObject) {
    let maxCareer = null;
    let maxScore = -1;

    Object.entries(scoreObject).forEach(([career, score]) => {
        if (score > maxScore) {
            maxScore = score;
            maxCareer = career;
        }
    });

    return maxCareer;
}
